document.getElementById("box1").addEventListener("mouseenter", function(){
    
    document.getElementById("box1").style.background="#070769";
    document.getElementById("box1").style.width="500px";
    document.getElementById("box1").style.height="500px";
    document.getElementById("catimage").style.width="500px";
    document.getElementById("catimage").style.height="500px";

})

